#include "inc/search.h"

/* Συνάρτηση γραμμικής αναζήτησης, με είσοδο:
   δείκτη σε πίνακα ακεραίων
   μέγεθος πίνακα
   στοιχείο προς αναζήτηση */
int linearsearch(int* array, int l, int s)
{
	int i = 0; /* Μετρητής για προσπέλαση του πίνακα */
	for (i = 0; i < (l + 1); i++) /* Γραμμική προσπέλαση */
	{
		if (array[i] == s) /* Συνθήκη εύρεσης στοιχείου */
		{
			return i; /* Επιστροφή της θέσης του στοιχείου */
		}
	}
	return -1; /* Επιστροφή -1, αποτυχία εύρεσης */
}